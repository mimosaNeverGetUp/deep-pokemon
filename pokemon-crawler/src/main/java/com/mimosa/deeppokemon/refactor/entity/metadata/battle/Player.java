package com.mimosa.deeppokemon.refactor.entity.metadata.battle;

/**
 * 对局玩家信息
 *
 * @author huangxiaocong(2070132549@qq.com)
 */
public class Player {
    /**
     * 对局玩家编号
     * 在常见的双人对战中，编号有1,2
     */
    private int number;

    /**
     * 对局使用队伍
     *
     */
    private Team team;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public Team getTeam() {
        return team;
    }

    public void setTeam(Team team) {
        this.team = team;
    }
}
